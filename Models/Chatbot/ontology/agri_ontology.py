def correct_spelling(text: str) -> str:
    """Apply spell correction using dictionary lookups."""
    norm = normalize_ar(text)
    for wrong, right in SPELL_FIXES.items():
        norm = norm.replace(wrong, right)
    return norm


def expand_query(query: str) -> List[str]:
    """Return ranked list of queries: original + semantic expansions."""
    expanded = [query]
    norm = normalize_ar(query)
    for canonical, aliases in CROP_ALIASES.items():
        if any(normalize_ar(a) in norm for a in aliases):
            sems = CROP_SEMANTIC_EXPANSIONS.get(canonical, [])
            expanded.extend(sems[:6])   # max 6 expansions per crop
    return list(dict.fromkeys(expanded))


def detect_crops(text: str) -> List[str]:
    """Return list of detected crop canonical names."""
    norm = normalize_ar(text)
    found = []
    for canonical, aliases in CROP_ALIASES.items():
        if any(normalize_ar(a) in norm for a in aliases):
            found.append(canonical)
    return found


def detect_symptoms(text: str) -> List[str]:
    """Return list of detected symptoms."""
    norm = normalize_ar(text)
    found = []
    for disease, symptoms in DISEASE_SYMPTOMS.items():
        for s in symptoms:
            if normalize_ar(s) in norm:
                found.append(s)
    # Also check pests
    for pest, data in PESTS.items():
        for s in data.get('symptoms', []):
            if normalize_ar(s) in norm:
                found.append(s)
    return list(set(found))


def detect_nutrients(text: str) -> List[str]:
    """Return list of detected nutrient deficiencies."""
    norm = normalize_ar(text)
    found = []
    for nutrient, data in NUTRIENT_DEFICIENCIES.items():
        if normalize_ar(nutrient) in norm:
            found.append(nutrient)
        for s in data.get('symptoms', []):
            if normalize_ar(s) in norm:
                found.append(nutrient)
                break
    return list(set(found))


def get_treatment(crop: str, disease: str) -> Optional[List[str]]:
    """Return treatment steps for crop-disease combination."""
    return TREATMENT_PROTOCOLS.get(crop, {}).get(disease)


def get_seasonal_info(crop: str) -> Optional[Dict]:
    """Return sowing/harvest calendar for crop in Egypt."""
    return SEASONAL_CALENDAR.get(crop)
