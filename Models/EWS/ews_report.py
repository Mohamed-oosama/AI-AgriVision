def build_html(p: EwsReportPayload, text_en: str, text_ar: str) -> str:
    if not _HAS_JINJA:
        return (f"<html><body><h1>{p.area_name}</h1>"
                f"<pre>{text_en}</pre>"
                f"<pre dir='rtl'>{text_ar}</pre></body></html>")
    env = Environment(loader=BaseLoader(), autoescape=select_autoescape(["html", "xml"]))
    tpl = env.from_string(HTML_TEMPLATE)
    return tpl.render(p=p, text_en=text_en, text_ar=text_ar)

def render_full_report(p: EwsReportPayload) -> Dict[str, str]:
    en = build_text_en(p)
    ar = build_text_ar(p)
    html = build_html(p, en, ar)
    return {"text_en": en, "text_ar": ar, "html": html}
