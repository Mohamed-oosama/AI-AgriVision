"""
ews_report.py
=============
Bilingual (Arabic / English) Expert Reports for the Early Warning System.

Produces three artifacts from a finalized EWS diagnosis:
  1. report_text_en  -- plain-text English narrative
  2. report_text_ar  -- plain-text Arabic narrative 
  3. report_html     -- a self-contained Jinja2-rendered HTML page
"""

from typing import Any, Dict, List, Optional

try:
    from jinja2 import Environment, BaseLoader, select_autoescape
    _HAS_JINJA = True
except ImportError:
    _HAS_JINJA = False

class EwsReportPayload:
    def __init__(
        self,
        area_name: str,
        area_id: int,
        risk_level: str,
        stress_probability: float,
        action: str,
        action_ar: str,
        reasoning: List[str],
        reasoning_ar: List[str],
        shap_explanation: Dict[str, Any]
    ):
        self.area_name = area_name
        self.area_id = area_id
        self.risk_level = risk_level
        self.stress_probability = stress_probability
        self.action = action
        self.action_ar = action_ar
        self.reasoning = reasoning
        self.reasoning_ar = reasoning_ar
        self.shap_explanation = shap_explanation

def build_text_en(p: EwsReportPayload) -> str:
    lines = []
    lines.append(f"EARLY WARNING SYSTEM REPORT")
    lines.append(f"Target Protected Area: {p.area_name} (ID: {p.area_id})")
    lines.append(f"Risk Level: {p.risk_level.upper()}")
    lines.append(f"Stress Probability: {p.stress_probability:.1%}")
    lines.append("-" * 40)
    lines.append("")
    lines.append("CONTEXT & OBJECTIVE:")
    lines.append("This report is generated by the AgriMind AI Early Warning System using multi-modal machine learning fusion. It aims to provide an accurate ecological assessment to detect and prevent drought and vegetation stress in Egyptian Protected Areas.")
    lines.append("")
    lines.append("1. DETECTED STRESS INDICATORS:")
    if p.reasoning:
        for r in p.reasoning:
            lines.append(f"  [!] {r}")
    else:
        lines.append("  [✓] No critical stress indicator triggers detected. The area's telemetry shows stable conditions.")
    
    lines.append("")
    lines.append("2. RECOMMENDED MITIGATION STRATEGIES:")
    lines.append(f"  • Primary Action: {p.action}")
    lines.append("  • Additional Guidelines: Ensure routine ecological audits and adhere to the Ministry of Environment's water preservation directives. Maintain continuous satellite telemetry monitoring for early deviation detection.")
    
    if p.shap_explanation and p.shap_explanation.get("feature_impacts"):
        lines.append("")
        lines.append("3. ENVIRONMENTAL DRIVERS (SHAP AI EXPLANATION):")
        lines.append("The following environmental factors have the highest impact on the calculated stress probability:")
        for impact in p.shap_explanation["feature_impacts"][:5]:
            sign = "+" if impact["shap_value"] >= 0 else ""
            feature_name = str(impact['feature']).replace('_', ' ').title()
            lines.append(f"  • {feature_name}:")
            lines.append(f"      Current Value : {impact['feature_value']:.3f}")
            lines.append(f"      Model Impact  : {sign}{impact['shap_value']:.4f}")
            
    return "\n".join(lines)

def build_text_ar(p: EwsReportPayload) -> str:
    lines = []
    lines.append(f"تقرير نظام الإنذار المبكر")
    lines.append(f"المحمية المستهدفة: {p.area_name} (معرف: {p.area_id})")
    risk_ar_map = {
        "Low": "منخفض",
        "Moderate": "متوسط",
        "High": "مرتفع",
        "Critical": "حرج"
    }
    risk_ar = risk_ar_map.get(p.risk_level, p.risk_level)
    lines.append(f"مستوى الخطر: {risk_ar}")
    lines.append(f"احتمالية الإجهاد: {p.stress_probability:.1%}")
    lines.append("-" * 40)
    lines.append("")
    lines.append("السياق والهدف:")
    lines.append("تم إنشاء هذا التقرير بواسطة نظام الإنذار المبكر (AgriMind) المعتمد على الذكاء الاصطناعي وخوارزميات دمج البيانات. يهدف هذا النظام إلى توفير تقييم بيئي دقيق لاكتشاف والوقاية من الجفاف والإجهاد النباتي في المحميات الطبيعية المصرية.")
    lines.append("")
    lines.append("1. مؤشرات الإجهاد المرصودة:")
    if p.reasoning_ar:
        for r in p.reasoning_ar:
            lines.append(f"  [!] {r}")
    else:
        lines.append("  [✓] لا توجد مؤشرات إجهاد حرجة. قراءات المحمية البيئية مستقرة وطبيعية.")
    
    lines.append("")
    lines.append("2. استراتيجيات التخفيف والإجراءات الموصى بها:")
    lines.append(f"  • الإجراء الأساسي: {p.action_ar}")
    lines.append("  • إرشادات إضافية: يُنصح بالالتزام بتوجيهات وزارة البيئة لترشيد وتأمين الموارد المائية، والاستمرار في المراقبة الدورية عبر الأقمار الصناعية لضمان التدخل السريع عند أي تغيرات مناخية طارئة.")
    
    if p.shap_explanation and p.shap_explanation.get("feature_impacts"):
        lines.append("")
        lines.append("3. العوامل البيئية الأكثر تأثيراً (تحليل الذكاء الاصطناعي SHAP):")
        lines.append("يوضح هذا القسم العوامل المناخية والبيئية التي ساهمت بأكبر قدر في حساب احتمالية الإجهاد:")
        for impact in p.shap_explanation["feature_impacts"][:5]:
            sign = "+" if impact["shap_value"] >= 0 else ""
            feature_name = str(impact['feature']).replace('_', ' ').title()
            lines.append(f"  • {feature_name}:")
            lines.append(f"      القيمة الحالية : {impact['feature_value']:.3f}")
            lines.append(f"      تأثير النموذج  : {sign}{impact['shap_value']:.4f}")
            
    return "\n".join(lines)

